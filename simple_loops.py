#start counter at 0
counter = 0

#While 'counter' is less than or equal to 5,
#run the loop

while (counter < 5):
    #show counter value
    print (counter)
    #shortcut to increase counter by 1
    #counter = (counter + 1)
    counter += 1

#control C stops a script in an infinite loop

#Run loop in range 0-5
#counter changes automatically in each iteration

for counter in range (0,5):
    print (counter)
